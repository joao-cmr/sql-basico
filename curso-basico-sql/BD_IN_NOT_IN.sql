USE PEDIDOS
--LISTA:IN/ NOT IN

SELECT NOME
	  ,COD_DEPTO
FROM TB_EMPREGADO
WHERE COD_DEPTO=1
   OR COD_DEPTO=3
   OR COD_DEPTO=4
   OR COD_DEPTO=7
   ORDER BY COD_DEPTO

--IN

SELECT NOME
	  ,COD_DEPTO
FROM TB_EMPREGADO
WHERE COD_DEPTO IN(1,3,4,7)
ORDER BY COD_DEPTO


SELECT NOME
	  ,CODCLI
	  ,ESTADO
FROM TB_CLIENTE
WHERE ESTADO IN('AM', 'PR', 'RJ', 'SP')
ORDER BY ESTADO

SELECT NOME
	  ,CODCLI
	  ,ESTADO
FROM TB_CLIENTE
WHERE ESTADO NOT IN('AM', 'PR', 'RJ', 'SP')
ORDER BY ESTADO

