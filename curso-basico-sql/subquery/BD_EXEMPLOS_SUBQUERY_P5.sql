--LISTAR O NOME DO FUNCIONARIO, SALARIO E O SALARIO INICIAL (TB_CARGO)
SELECT CODFUN
	  ,NOME
	  ,COD_CARGO
	  ,SALARIO
	  ,(SELECT SALARIO_INIC
	    FROM TB_CARGO
		WHERE COD_CARGO=TB_EMPREGADO.COD_CARGO) AS SALARIO_INICIAL
FROM TB_EMPREGADO

