USE SISTEMA_PEDIDOS

SELECT *
FROM TB_EMPREGADO

SELECT NOME
	  ,CARGO
	  ,SALARIO
FROM TB_EMPREGADO AS EMP
INNER JOIN TB_CARGO AS CARGO
ON EMP.COD_CARGO=CARGO.COD_CARGO

USE PEDIDOS

SELECT *
FROM TB_EMPREGADO

SELECT NOME
	  ,DEPTO
	  ,SALARIO
FROM TB_EMPREGADO AS EMP
INNER JOIN TB_DEPARTAMENTO AS DEPTO
ON EMP.COD_DEPTO=DEPTO.COD_DEPTO

